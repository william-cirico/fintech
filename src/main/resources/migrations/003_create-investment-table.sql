CREATE TABLE T_FIN_INVESTMENT(
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    amount NUMBER(10, 2) NOT NULL,
    "DATE" DATE NOT NULL,
    type VARCHAR(20),
    risk VARCHAR(10),
    liquidity VARCHAR(20),
    due_date DATE,
    profitability NUMBER(10, 2),
    account_id NUMBER NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP  NOT NULL,

    CONSTRAINT FK_INVESTMENT_ACCOUNT FOREIGN KEY (ACCOUNT_ID) REFERENCES T_FIN_ACCOUNT(ID)
)